const handler = async (m, { conn, text }) => {
  if (!text.includes('|')) {
    return m.reply(`Ejemplo:\n.reactch <link>|<texto>|<n√∫mero de fuente>\n\nFuentes disponibles:\n0: Cuadrada\n1: Letras negras`)
  }

  let [link, rawText, fontIndex = 0] = text.split('|')
  link = link.trim()
  const msg = rawText.trim().toLowerCase()
  fontIndex = parseInt(fontIndex.trim())

  if (!link.startsWith("https://whatsapp.com/channel/")) {
    return m.reply("‚ùå Link no v√°lido")
  }

  if (isNaN(fontIndex) || fontIndex >= fonts.length) {
    return m.reply("‚ùå Fuente inv√°lida. Usa un n√∫mero entre 0 y " + (fonts.length - 1))
  }

  const font = fonts[fontIndex]
  const emoji = msg.split('').map(c => c === ' ' ? '‚Äï' : (font[c] || c)).join('')

  try {
    const [, , , , channelId, messageId] = link.split('/')
    const res = await conn.newsletterMetadata("invite", channelId)
    await conn.newsletterReactMessage(res.id, messageId, emoji)
    m.reply(`‚úÖ Reacci√≥n *${emoji}* Enviada a *${res.name}* con fuente ${fontIndex}.`)
  } catch (e) {
    console.error(e)
    m.reply("‚ùå Error!")
  }
}

handler.command = ['reactch', 'rch']
handler.tags = ['tools']
handler.help = ['reactch <link>|<texto>|<n√∫mero de fuente>']
